package org.kanbansalad.scanner.client.android.program;

import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;

import org.kanbansalad.scanner.client.program.BluetoothConnection;

import android.bluetooth.BluetoothSocket;

public class AndroidBluetoothConnection implements BluetoothConnection {
    private final BluetoothSocket socket;

    public AndroidBluetoothConnection(BluetoothSocket socket) {
        this.socket = socket;
    }

    @Override
    public InputStream getInputStream() throws IOException {
        return socket.getInputStream();
    }

    @Override
    public OutputStream getOutputStream() throws IOException {
        return socket.getOutputStream();
    }

    @Override
    public void close() throws IOException {
        socket.close();
    }

}
